    /// <summary>
    /// A class for mapping grayscale intensity to a color via lookup tables
    /// </summary>
    public class Colormap
    {
        private readonly ColormapLUT lut;

        private readonly byte[] _RedByte;
        private readonly byte[] _GreenByte;
        private readonly byte[] _BlueByte;

        public Colormap(ColormapType colormapType)
        {
            //bool UseInterpolation = false;
            int colormapSize = 256;

            this._RedByte = new byte[colormapSize];
            this._GreenByte = new byte[colormapSize];
            this._BlueByte = new byte[colormapSize];

            switch (colormapType)
            {
                case ColormapType.Hot:
                    lut = new HotColormapLUT();
                    break;
                case ColormapType.Jet:
                    lut = new JetColormapLUT();
                    break;
                case ColormapType.HSV:
                    lut = new HSVColormapLUT();
                    break;
                case ColormapType.Copper:
                    lut = new CopperColormapLUT();
                    break;
                case ColormapType.Binary:
                    lut = new BinaryColormapLUT();
                    break;
                case ColormapType.Bone:
                    lut = new BoneColormapLUT();
                    break;
                case ColormapType.Gray:
                default:
                    lut = new GrayColormapLUT();
                    break;
            }

            int mappingRatio = colormapSize / lut.Length;
            try
            {
                for (int j = 0; j < colormapSize; j++)
                {
                    this._RedByte[j] = (byte)(255 * lut.Red[j / mappingRatio]);
                    this._GreenByte[j] = (byte)(255 * lut.Green[j / mappingRatio]);
                    this._BlueByte[j] = (byte)(255 * lut.Blue[j / mappingRatio]);
                }
            }
            catch (IndexOutOfRangeException e) { Console.WriteLine(e); }
        }

        public byte[] RedByte { get { return _RedByte; } }
        public byte[] GreenByte { get { return _GreenByte; } }
        public byte[] BlueByte { get { return _BlueByte; } }

        #region Nested ColormapLUT Classes

        internal abstract class ColormapLUT
        {
            #region Properties
            public double[] Red
            {
                get { return this._Red; }
            }
            protected readonly double[] _Red;

            public double[] Green
            {
                get { return this._Green; }
            }
            protected readonly double[] _Green;

            public double[] Blue
            {
                get { return this._Blue; }
            }
            protected readonly double[] _Blue;

            public int Length
            {
                get
                {
                    if (Red != null)
                        return Red.Length;
                    else
                        return 0;
                }
            }
            #endregion

            #region Constructor
            public ColormapLUT(double[] R, double[] G, double[] B)
            {
                this._Red = R;
                this._Green = G;
                this._Blue = B;
            }
            #endregion
        }
